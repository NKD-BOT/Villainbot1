@app.on_message(filters.command("repo", prefixes="#"))
@capture_err
async def repo(_, message):
    async with httpx.AsyncClient() as client:
        response = await client.get("https://api.github.com/repos/DAXXTEAM/DAXXMUSIC/contributors")
    
    if response.status_code == 200:
        users = response.json()
        list_of_users = ""
        count = 1
        for user in users:
            list_of_users += f"{count}. [{user['login']}]({user['html_url']})\n"
            count += 1

        text = f"""[­Юќ▒­Юќц­Юќ»­Юќ« ­ЮќФ­Юќе­ЮќГ­Юќф](https://github.com/DAXXTEAM/DAXXMUSIC) | [­Юќд­Юќ▒­Юќ«­Юќ┤­Юќ»](https://t.me/NKD_Korean_Group)
| ­Юќб­Юќ«­ЮќГ­Юќ│­Юќ▒­Юќе­ЮќА­Юќ┤­Юќ│­Юќ«­Юќ▒­Юќ▓ |
----------------
{list_of_users}"""
        await app.send_message(message.chat.id, text=text, disable_web_page_preview=True)
    else:
        await app.send_message(message.chat.id, text="Failed to fetch contributors.")


